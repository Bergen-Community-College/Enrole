<p><strong>Lifetime Learning Tax Credit</strong><br/>
The Lifetime Learning tax credit is applicable to any level of post-secondary education. It enables a taxpayer to claim a tax credit of up to $2,000 per family for the cost of qualified tuition and related expenses paid by the taxpayer during the tax year on behalf of the taxpayer, the taxpayer’s spouse, or the taxpayer’s dependents. You may qualify for an educational tax credit for tuition and fees paid for undergraduate, graduate, and continuing education courses.</p>
